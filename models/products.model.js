const mongoose = require("mongoose");

const productsSchema = mongoose.Schema(
  {
    _id: {
      type: mongoose.Schema.Types.ObjectId,
      required: true,
      auto: true,
    },
    clickURL: String,
    clickTrackingURL: String,
    id: String,
    productURL: String,
    DefaultCategoryName: String,
    DefaultCategoryID: Number,
    rating: Number,
    CategoryGroupID: Number,
    Discount: Object,
    IsReturnable: Number,
    DefaultCategoryLinkRewrite: String,
    MetaKeywords: String,
    OrdersCount: Number,
    Gender: String,
    Name: String,
    ShortDescription: String,
    GenderID: Number,
    GenderLinkRewrite: String,
    Features: Object,
    StyleCode: String,
    GenderName: String,
    MetaTitle: String,
    Color: String,
    Quantity: Number,
    SellingPrice: Number,
    Sizes: Object,
    Active: Number,
    Price: Number,
    PublishedDate: String,
    imageURL: String,
    productLabel: String,
  },
  { versionKey: false }
);

const ProductModelAll = mongoose.model("allproduct", productsSchema);
const ProductModelWomen = mongoose.model("womenfestive", productsSchema);
const ProductModelMen = mongoose.model("popularmen", productsSchema);
const ProductModelNewArrivals = mongoose.model("newarrival", productsSchema);
const ProductModelKids = mongoose.model("kid", productsSchema);
const ProductModelHomeDecors = mongoose.model("homedecor", productsSchema);
const ProductModelAccessories = mongoose.model("accessorie", productsSchema);

module.exports = {
  ProductModelAll,
  ProductModelWomen,
  ProductModelMen,
  ProductModelNewArrivals,
  ProductModelKids,
  ProductModelHomeDecors,
  ProductModelAccessories,
};

